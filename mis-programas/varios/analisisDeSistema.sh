#!/bin/bash

clear

echo "##############################################################"
echo "Información del Sistema"
echo "##############################################################"

echo "=============================================================="
echo "Sistema Operativo: "
uname -a
echo "=============================================================="


echo "=============================================================="
echo "Distribución de Linux: "
lsb_release -a 2>/dev/null || echo "lsb_release"
echo "=============================================================="


echo "=============================================================="
echo "Versión del Kernel: "
uname -r
echo "=============================================================="


echo "=============================================================="
echo "Tiempo de Actividad del Sistema: "
uptime
echo "=============================================================="


echo "=============================================================="
echo "Espacio en Disco: "
df -h
echo "=============================================================="


echo "=============================================================="
echo "Información de la Red: "
ip a
echo "=============================================================="


echo "=============================================================="
echo "Conexiones de Red Activas: "
ss -tuln
echo "=============================================================="


echo "=============================================================="
echo "Archivos de Configuración de Red: "
cat /etc/hosts
cat /etc/resolv.conf
echo "=============================================================="


echo "=============================================================="
echo "Binarios con Permisos SUID: "
find / -perm -4000 2>/dev/null
echo "=============================================================="


echo "=============================================================="
echo "Binarios que se Pueden Ejecutar con otro Usuario"
sudo -1
echo "=============================================================="

